<h2>1 Maya<sup style="font-size: 10px;">1</sup> i3d exporter</h2>
	
<h3>1.1 Installation</h3>

<b>Step 1</b><br />
Place the files I3DExportUI.mel, I3DExportValidate.mel and I3DExporterXY.mll into the folder "C:/Documents and Settings/USERNAME/My Documents/maya/scripts".<br />
<br />
<b>Step 2</b><br />
Start Maya and open the Script Editor window.<br />
<br />

<img src="images/i3d/export_installation.gif" border="0">
<br />
<br />

<b>Step 3</b><br />
Select custom shelf tab<br />
<br />

<b>Step 4</b><br />
Type the following commands in the lower portion of the Script Editor:<br />
<br />

<table><tr><td>
<div id="code">
loadPlugin -qt "C:/Documents and Settings/USERNAME/My Documents/maya/scripts/I3DExporterXY.mll";<br />
source I3DExportUI;<br />
source I3DExportValidate;<br />
I3DExport;<br />
</div>
</td></tr></table>
<br />
<b>Note:</b> Maya don't like backslashes so replace them with slashes.<br /><br />
Highlight the commands, then use the middle mouse button, to drag the highlighted MEL commands to the shelf.<br />
<br />
<b>Step 5 (optional)</b><br />
Copy the I3D_icon.bmp into the folder "C:/Documents and Settings/USERNAME/My Documents/maya/X.Y/prefs/icons".
Edit shelf to replace the default icon with the one enclosed.<br />
<br />
<br />

<h3>1.2 Usage</h3>

<img src="images/i3d/export_script.gif" border="0">
<br />
<br />
<b>Export Options</b><br />
<br />
Export: IK, Animation, Shapes, Nurbs Curves, Lights, Cameras, Particle Systems and/or Default Cameras<br />
<br />
<b>Shape Export Subparts</b><br />
<br />
Defines which parts of a shape are exported.
Normals, Vertex Colors, Texture Coordinates and/or Skin Weights.<br />
<br />
<b>Miscellaneous</b><br />
<br />
<ul>
<li><b>Verbose</b>, Display verbose information in output window during the export.</li>
<li><b>Relative Paths</b>, Generate relative paths for texture files.</li>
<li><b>Float Epsilon</b>, Truncate values within epsilon range to zero. Epsilon: [-1e-5, 1e-5]</li>
</ul>
<br />

